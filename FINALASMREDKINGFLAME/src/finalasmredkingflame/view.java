    public void fillToTable() {
        DefaultTableModel dtm = (DefaultTableModel) bang.getModel();
        dtm.setNumRows(0);

        for (doituongNV item : dt.getDsNV()) {
            dtm.addRow(new Object[]{item.getIdCode(), item.getName(), item.getAge(), item.getEmail(), item.getSalary()});
            ImageIcon II = new ImageIcon(item.getImg());
            Image anh = II.getImage();
            anh = anh.getScaledInstance(anhNV.getWidth(), anhNV.getHeight(), Image.SCALE_SMOOTH);
            II = new ImageIcon(anh);
            anhNV.setIcon(II);
        }

    }

