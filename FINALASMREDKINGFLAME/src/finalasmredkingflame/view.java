    public void find() {
        String MaCanTim = JOptionPane.showInputDialog(rootPane, "Hãy Nhập Mã Nhân Viên Cần Tìm");
        int count = 0;
        for (doituongNV item : dt.getDsNV()) {
            if (item.getIdCode().equals(MaCanTim)) {
                luaChon = count;
                break;
            }
            count++;
        }
        if (count == dt.getDsNV().size()) {
            JOptionPane.showMessageDialog(rootPane, "Không Tìm Thấy Mã Nhân Viên Cần Tìm");
        } else {
            showNV(luaChon);
        }
    }

    public void delete() {
        String MaCanXoa = JOptionPane.showInputDialog(rootPane, "Hãy Nhập Mã Nhân Viên Cần Xóa");
        int count = 0;
        for (doituongNV item : dt.getDsNV()) {
            if (item.getIdCode().equals(MaCanXoa)) {
                dt.getDsNV().remove(item);
                JOptionPane.showMessageDialog(rootPane, "Đã Xóa Thành Công");
                fillToTable();
                break;
            }
            count++;
        }

        if (count == dt.getDsNV().size()) {
            JOptionPane.showMessageDialog(rootPane, "Không Tìm Thấy Mã Nhân Viên Cần Xóa");
        }

    }

    Comparator<doituongNV> sortTheoDiem = new Comparator<doituongNV>() {
        @Override
        public int compare(doituongNV o2, doituongNV o1) {
            return Double.compare(o1.getSalary(), o2.getSalary());
        }
    };

    Comparator<doituongNV> sortTheoAlphabet = new Comparator<doituongNV>() {
        @Override
        public int compare(doituongNV o1, doituongNV o2) {
            return o1.getName().compareToIgnoreCase(o2.getName());
        }
    };

    public void CompareME() {

        String luachon
                = JOptionPane.showInputDialog(rootPane, "Hãy Nhập Lựa Chọn SORT (1 = Name) or (2 = Salary)");

        if (luachon.equals("1")) {
            Collections.sort(dt.getDsNV(), sortTheoAlphabet);
            fillToTable();
            clearForm();
        } else if (luachon.equals("2")) {
            Collections.sort(dt.getDsNV(), sortTheoDiem);
            fillToTable();
            clearForm();
        } else {
            JOptionPane.showMessageDialog(rootPane, "Không Tìm Thấy Mã Nhân Viên");
        }

    }

