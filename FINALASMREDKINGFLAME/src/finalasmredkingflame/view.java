    // check Validate
    public doituongNV GetNV() {
        // check Email
        String regex = "^[A-Za-z0-9+_.-]+@(.+)$";
        Pattern pattern = Pattern.compile(regex);
        Matcher matcher = pattern.matcher(inputEmailNV.getText());

        // Check Xem Có Rỗng Không?
        if (inputEmailNV.getText().equals("") || inputLuongNV.getText().equals("") || inputMaNV.getText().equals("")
                || inputTenNV.getText().equals("") || inputTuoiNV.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Bạn Không Được Bỏ Trống");
        } else {
            Integer tuoi = Integer.parseInt(inputTuoiNV.getText());
            Double luong = Double.parseDouble(inputLuongNV.getText());

            // Check Mã Nhân Viên Nếu Lặp
            int count = 0;
            for (doituongNV item : dt.getDsNV()) {
                if (item.getIdCode().equals(inputMaNV.getText())) {
                    count++;
                    break;
                }
            }

            if (count != 0) {
                JOptionPane.showMessageDialog(rootPane, "Trùng Mã Nhân Viên");
            } else if (tuoi < 16 || tuoi > 65) {
                JOptionPane.showMessageDialog(this, "Tuổi Phải Trong Khoảng 16 --> 65");
            } else if (luong < 5000) {
                JOptionPane.showMessageDialog(this, "Lương Phải Lớn Hơn 5tr");
            } else if (matcher.matches() == false) {
                JOptionPane.showMessageDialog(this, "Sai Cú Pháp Email");
            } else {
                doituongNV dt = new doituongNV(inputTenNV.getText(), inputEmailNV.getText(), luong, tuoi, inputMaNV.getText(), filename);
                return dt;

            }
        }

        // Parse Dữ Liệu
        // Check Tuổi, Lương, Email Nếu Sai Sót
        return null;
    }

